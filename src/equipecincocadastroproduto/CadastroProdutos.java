    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        Conexão banco = new Conexão("localhost","3306","cadastroProduto", "cadastroProduto", "cadastroProduto");
        banco.realizaConexao();
    
        if(banco.isStatusConexao()){
            CadastroProduto cadastro = new CadastroProduto(banco.getConexao());
            try{
                ResultSet listaProdutos = cadastro.obterListaProdutos();
                DefaultTableModel modelo = (DefaultTableModel) tbDados.getModel();
                modelo.setNumRows(0);
                
                while(listaProdutos.next()){
                    modelo.addRow(new Object[]{listaProdutos.getInt("idProduto"), listaProdutos.getString("tipo"), listaProdutos.getString("nome"), listaProdutos.getString("descricao"), listaProdutos.getString("resumo"), listaProdutos.getInt("quantidade")});
                }
            }
            catch(SQLException e){
                tela.setText("Não foi listar os produtos "+e.getMessage());
            }
        }
        else{
            System.out.println("Não está conectado");
            System.out.println(banco.getMensagemErro());
        }
    }//GEN-LAST:event_btnListarActionPerformed

