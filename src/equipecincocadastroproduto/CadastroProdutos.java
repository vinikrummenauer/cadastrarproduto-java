    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String nome = txtNome.getText();
        String tipo = txtTipo.getText();
        String descricao = txtDescricao.getText();
        String resumo = txtResumo.getText();
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        int idProduto = Integer.parseInt(txtId.getText());
        
        Conexão banco = new Conexão("localhost","3306","cadastroProduto", "cadastroProduto", "cadastroProduto");
        banco.realizaConexao();
        
        if(banco.isStatusConexao()){
            CadastroProduto cadastro = new CadastroProduto(banco.getConexao());
            try{
                cadastro.setNome(nome);
                cadastro.setTipo(tipo);
                cadastro.setDescricao(descricao);
                cadastro.setResumo(resumo);
                cadastro.setQuantidade(quantidade);
                cadastro.setIdProduto(idProduto);
                cadastro.atualizaProdutos();
                tela.setText("Produto atualizado com sucesso!");
            }
            catch(SQLException e){
                tela.setText("Não foi possível atualizar o produto, erro: "+e.getMessage());
            }
        }
        else{
            System.out.println("Não está conectado. ");
            System.out.println(banco.getMensagemErro());
            
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

